    private boolean cnpControlDigit(String cnp)
    {
        String checkDigitNumber = "279146358279";
        int cnpCheckDigit = Character.getNumericValue(cnp.charAt(cnp.length() - 1));
        long sum = 0;
        
        for (int i = 0; i < cnp.length() - 1; ++i) {
            sum += Character.getNumericValue(cnp.charAt(i)) * Character.getNumericValue(checkDigitNumber.charAt(i));
        }

        long remainder = sum % 11;
        if (remainder == 10) {
            if (cnpCheckDigit != 1)
                return false;
        }
        else if (cnpCheckDigit != remainder)
            return false;
 
        return true;
    }
    
