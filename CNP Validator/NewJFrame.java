    private void cnpCheckButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cnpCheckButtonActionPerformed
        // TODO add your handling code here:
        String cnp = cnpTextField.getText();

        if (cnp.length() != 13) {
            showAnswer(false, "Invalid cnp size");
            return;
        }

        int sexDigit = cnp.charAt(0);
        if (sexDigit != '1' && sexDigit != '2' && sexDigit < '5') {
           showAnswer(false, "Invalid sex digit");
           return;
        }

        int currentYear = Calendar.getInstance().get(Calendar.YEAR);
        int lastTwoYearDigits = currentYear % 100;
        int cnpYear = Integer.parseInt(cnp.substring(1, 3));
        if (cnpYear < lastTwoYearDigits)
            cnpYear += 2000;
        else
            cnpYear += 1900;

        int month = Integer.parseInt(cnp.substring(3, 5));
        if (month == 0 || month > 12) {
            showAnswer(false, "Invalid month");
            return;
        }

        int day = Integer.parseInt(cnp.substring(5, 7));
        int daysInMonth = new GregorianCalendar(cnpYear, month - 1, 1).getActualMaximum(Calendar.DAY_OF_MONTH);
        if (day == 0 || day > daysInMonth) {
            showAnswer(false, "Invalid day");
            return;
        }

        int county = Integer.parseInt(cnp.substring(7, 9));
        if (county == 0 || county > 52) {
            showAnswer(false, "Invalid county");
            return;
        }

        int num = Integer.parseInt(cnp.substring(9, 12));
        if (num == 0) {
            showAnswer(false, "Invalid given number");
            return;
        }

        if (!cnpControlDigit(cnp)) {
           showAnswer(false, "Invalid check digit");
           return;
        }

        showAnswer(true, "OK");
    }//GEN-LAST:event_cnpCheckButtonActionPerformed

